# 사칙연산 함수 출력

def add(a, b):
    return a+b

def minus(a, b):
    return a-b

def multply(a, b):
    return a*b

def divide(a, b):
    return a/b

sum = add(1, 5)
min = minus(1, 5)
mul = multply(1, 5)
div = divide(1, 5)

print("sum :", sum)
print("min :", min)
print("mul :", mul)
print("div :", div)